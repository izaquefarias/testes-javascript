// == (comparação implícita) - > compara o valor

const numero = 5;
const texto = "5";

console.log(numero == texto);

// === (comparação explícita) -> compara o valor e o tipo de dado

const numeroB = 5;
const textoB = "5";

console.log(numeroB === textoB);

// utilitário typeof (informa qual o tipo de dado da variável)

console.log(typeof numero);
console.log(typeof texto);
console.log(typeof numeroB);
console.log(typeof textoB);